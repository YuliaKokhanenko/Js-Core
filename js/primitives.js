console.log("primitives");
// string ---- строка
let stringData = "string data";
console.log(stringData);
// typeof - оператор определения типа данных
console.log(typeof stringData);

stringData = "";
console.log(stringData);
console.log(typeof stringData);

stringData = " ";
console.log(stringData);
console.log(typeof stringData);

//number -число, 2 тип примитивных дынных
let number = 1;
console.log(number);
console.log(typeof number);

number = 2e53;
console.log(number);
console.log(typeof number);

number = Infinity;
console.log(number);
console.log(typeof number);

number = NaN; // not a number ---не число
console.log(number);
console.log(typeof number);

//boolean ---- true || false
let isOnLine = true;
console.log(isOnLine);
console.log(typeof isOnLine);

isOnLine = false;
console.log(isOnLine);
console.log(typeof isOnLine);

//undefined
let message;
console.log(message);
console.log(typeof message);

//null ---пустое место
let user = null;
console.log(user);
console.log(typeof user);
